package com.sample;


public class User {

  private String acountNumber;
  private String acountName;
  private String acountPassword;
  private String acountEmail;


  public String getAcountNumber() {
    return acountNumber;
  }

  public void setAcountNumber(String acountNumber) {
    this.acountNumber = acountNumber;
  }


  public String getAcountName() {
    return acountName;
  }

  public void setAcountName(String acountName) {
    this.acountName = acountName;
  }


  public String getAcountPassword() {
    return acountPassword;
  }

  public void setAcountPassword(String acountPassword) {
    this.acountPassword = acountPassword;
  }


  public String getAcountEmail() {
    return acountEmail;
  }

  public void setAcountEmail(String acountEmail) {
    this.acountEmail = acountEmail;
  }

}
